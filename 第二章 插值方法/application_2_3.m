% 家庭储蓄规律问题
clear;clc;
x=[0.6,1.0,1.4,1.8,2.2,2.6,3.0,3.4,3.8,4 ];   % 月收入，单位：万元
y=[0.08,0.22,0.31,0.4,0.48,0.56,0.67,0.75,0.8,1.0 ];  % 月可储蓄金额，单位：万元

A = [length(x), sum(x); sum(x), sum(x .* x)];   % 法方程系数矩阵
b = [sum(y); sum(x .* y)];    % 法方程常数项
% pa = inv(A)*b;   % 左乘逆矩阵，求解 法方程
pa = A\b; % 左除系数矩阵，求解 法方程

pa = pa(end : -1 : 1); % 多项式系数
px = 0 : 0.01 : 10;   % 画直线函数的横坐标
py = polyval(pa, px);   % 多项式求值
plot(x, y, 'o', px, py);   % 一次多项式拟合结果